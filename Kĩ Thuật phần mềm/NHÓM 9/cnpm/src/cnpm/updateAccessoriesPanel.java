   public boolean checkNameAndBrandAcessExisting(String accessoryName, String brandName)
   {
       setCheck(false);
         try {
             updateBrandCart bc=new updateBrandCart();
            Connection connect=Connect();
            String sql1=" select TENLK,MAHANG From LinhKien ";
            PreparedStatement pr1=connect.prepareStatement(sql1);
            ResultSet rs=pr1.executeQuery();
            while(rs.next())
            {
                    if(rs.getString(1).equals(accessoryName) && bc.changeBrandIDIntoBrandName(rs.getString(2)).equals(brandName))
                            {
                                setCheck(true);
                                break;
                            }
            }
            pr1.close();
            rs.close();
            connect.close();
        } catch (SQLException ex) {
            Logger.getLogger(RegisterMember.class.getName()).log(Level.SEVERE, null, ex);
        }
       return isCheck();
   }
